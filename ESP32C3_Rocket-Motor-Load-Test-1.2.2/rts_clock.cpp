#include "rts_clock.h"

RTCClock rtcClock;

/******************************************************************************
 * Construtor
 ******************************************************************************/

RTCClock::RTCClock()
{
    hasTime = false;
}

/******************************************************************************
 * Define data/hora
 ******************************************************************************/

void RTCClock::setDateTime(const String& iso)
{
    currentDateTime = iso;

    hasTime = true;
}

/******************************************************************************
 * Retorna data/hora
 ******************************************************************************/

String RTCClock::getDateTime() const
{
    return currentDateTime;
}

/******************************************************************************
 * Data válida
 ******************************************************************************/

bool RTCClock::valid() const
{
    return hasTime;
}